# Strings sempre serão vistas como uma seguência de caracteres.
# Isso permite utilizarmos índices para capturar partes de uma string.

# Comprimento de uma string
s = "Hello World"
print(s)

comprimento = len(s)
print(comprimento)

# Indexação
print(s[0], s[1],  s[2],   s[3],    s[4],     s[5],      s[6], s[7], s[8], s[9], s[10])

print(s[0])
print(s[1])
print(s[2])
print(s[3])
print(s[4])
print(s[5])
print(s[6])
print(s[7])
print(s[8])
print(s[9])
print(s[10])

# Intervalos

print(s)
print(s[1:])
print(s[:3])
print(s[1:4])

# OBS: O limite final nunca é incluído.

print(s)
print(s[-1])
print(s[-2])
print(s[-3])
print(s[-4])
print(s[-5])
print(s[-6])
print(s[-7])
print(s[-8])
print(s[-9])
print(s[-10])
print(s[-11])

# Imprimindo tudo utilizando espaçamento
print(s)
print(s[::1])
print(s[::2])
print(s[::3])
print(s[::4])
print(s[::5])
print(s[::6])

print(s)
print(s[::-1])
print(s[::-2])
print(s[::-3])

