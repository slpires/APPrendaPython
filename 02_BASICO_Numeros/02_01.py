# Números em Python

"""
Tipos principais de números:
- int (Ex: 1, 10, -5, ...)
- float (Ex: 1.2, -0.5, 2e2, 3E2, ...)
"""

# Aritmética básica

soma = 2 + 1
print("soma = ", soma)

subtracao = 2 - 1
print("subtracao = ", subtracao)

multiplicacao = 2 * 2
print("multiplicacao = ", multiplicacao)

# Divisão sempre retorna um float (divisão de ponto flutuante).
divisao1 = 2 / 1
print("divisao1 = ", divisao1)

divisao2 = 3 / 2
print("divisao2 = ", divisao2)

divisao3 = 6 / 2.0
print("divisao3 = ", divisao3)

# Divisão inteira.
divisao4 = 3 // 2
print("divisao4 = ", divisao4)

restoDivisao = 10 % 3
print("restoDivisao = ", restoDivisao)

exponenciacao = 3 ** 2
print("exponenciacao = ", exponenciacao)

radiciacao = 4 ** 0.5
print("radiciacao = ", radiciacao)


